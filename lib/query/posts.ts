import {
  useInfiniteQuery,
  useMutation,
  useQuery,
  useQueryClient,
} from "@tanstack/react-query";
import { QUERY_KEYS } from "@/lib/query/queryKeys";
import { INewListing, IUpdatePost } from "@/types";
import {
  createListing,
  deletePost,
  deleteSavedPost,
  getListingById,
  getPostById,
  getRecentPosts,
  getUserPosts,
  likePost,
  savePost,
  searchPosts,
  updatePost,
} from "@/lib/appwrite/listing";

